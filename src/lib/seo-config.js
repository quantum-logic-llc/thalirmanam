/**
 * Generates Schema.org JSON-LD for LocalBusiness
 * @returns {object} Schema.org JSON-LD object
 */
export const getLocalBusinessSchema = () => {
  const business = seoConfig.businessInfo || {}

  return {
    '@context': 'https://schema.org',
    '@type': 'LocalBusiness',
    '@id': `${seoConfig.siteUrl}/#localbusiness`,
    name: business.name || seoConfig.siteName,
    alternateName: business.alternateName,
    description: business.description,
    url: seoConfig.siteUrl,
    telephone: business.telephone,
    email: business.email,
    address: {
      '@type': 'PostalAddress',
      streetAddress: business.streetAddress,
      addressLocality: business.addressLocality,
      addressRegion: business.addressRegion,
      postalCode: business.postalCode,
      addressCountry: business.addressCountry
    },
    geo: {
      '@type': 'GeoCoordinates',
      latitude: business.latitude,
      longitude: business.longitude
    },
    openingHoursSpecification: {
      '@type': 'OpeningHoursSpecification',
      dayOfWeek: [
        'Monday',
        'Tuesday',
        'Wednesday',
        'Thursday',
        'Friday',
        'Saturday'
      ],
      opens: '09:00',
      closes: '18:00'
    },
    priceRange: business.priceRange,
    currency: business.currency,
    aggregateRating: business.aggregateRating ? {
      '@type': 'AggregateRating',
      ratingValue: business.aggregateRating.ratingValue,
      reviewCount: business.aggregateRating.reviewCount,
      bestRating: business.aggregateRating.bestRating,
      worstRating: business.aggregateRating.worstRating
    } : undefined,
    areaServed: seoConfig.areaServed?.map(area => ({
      '@type': 'City',
      name: area
    })),
    sameAs: Object.values(seoConfig.socialLinks || {})
  }
}

