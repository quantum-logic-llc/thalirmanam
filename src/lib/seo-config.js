/**
 * Generates Schema.org JSON-LD for FAQPage
 * @param {array} faqs - Array of FAQ objects with question and answer
 * @returns {object} Schema.org JSON-LD object
 */
export const getFAQSchema = (faqs) => {
  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    mainEntity: faqs.map(faq => ({
      '@type': 'Question',
      name: faq.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: faq.answer
      }
    }))
  }
}

/**
 * Generates Schema.org JSON-LD for BreadcrumbList
 * @param {array} breadcrumbs - Array of breadcrumb objects with name and url
 * @returns {object} Schema.org JSON-LD object
 */
export const getBreadcrumbSchema = (breadcrumbs) => {
  return {
    '@context': 'https://schema.org',
    '@type': 'BreadcrumbList',
    itemListElement: breadcrumbs.map((crumb, index) => ({
      '@type': 'ListItem',
      position: index + 1,
      name: crumb.name,
      item: crumb.url
    }))
  }
}

